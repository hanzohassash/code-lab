# Loop 1: Count up the range from 0 to 50
for i in range(0,51,1):    #Start at 0, then stop at 50, and step by 1
    print(i)


# Loop 2: Count down the range from 50 to 0
for i in range(50,-1,-1):    #Start at 50, then stop at 0, and step by -1
    print(i)


# Loop 3: Count up from 30 to 50
for i in range(30,51,1):     #Start at 30, then stop at 51, and step by 1
    print(i)


# Loop 4: Count down the range from 50 to 10
for i in range(50,10,-2):    #Start at 50, then stop at 10, and step by -2
    print(i)


# Loop 5: Count up the range from 100 to 200
for i in range(100,200,5):   #Start at 100, then stop at 200, and step by 5
    print(i)
    